//adição
console.log(2+2); 

//subtração
console.log(2-2);

//multiplicação
console.log(2*2);

//divisão
console.log(2/2);

//incremento (adiciona 1)
let numero = 10
console.log(++numero);

//decremento (subtrai 1)
console.log(--numero);

//comparacao de maior
console.log(10>5);

//comparacao de menor
console.log(5<10);

//comparação de menor ou igual
console.log(1 <= 2);

// comparação de maior ou iguao
console.log (20 >= 12);

//comparação de igualdade
console.log("ola" == "ola");

//comparação de igualdade rígida
console.log(5 === "5");

//comparação de diferenças
console.log(10 != 11);

// comparação de diferentes rígidos
console.log(10 !== 20);

//comparador lógigo && (concatenar condições)
console.log(10 > 5 && 10 < 15);

//comparador lógico OR ||
console.log(10 > 5 || 5 > 10);